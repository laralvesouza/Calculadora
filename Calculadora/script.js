function soma(){

    var val1 = parseFloat(document.getElementById('val1').value)

    var val2 = parseFloat(document.getElementById('val2').value)

    document.getElementById('result').textContent = val1 + val2;

}

function subtracao(){

    var val1 = parseFloat(document.getElementById('val1').value)

    var val2 = parseFloat(document.getElementById('val2').value)

    document.getElementById('result').textContent = val1 - val2;

}

function divisao(){

    var val1 = parseFloat(document.getElementById('val1').value)

    var val2 = parseFloat(document.getElementById('val2').value)

    document.getElementById('result').textContent = val1 / val2;

}
 
function multiplicacao(){

    var val1 = parseFloat(document.getElementById('val1').value)

    var val2 = parseFloat(document.getElementById('val2').value)

    document.getElementById('result').textContent = val1 * val2;

}
 
